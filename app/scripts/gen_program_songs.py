def classify(song_title: str) -> str:
    for mood, kws in RULES:
        for kw in kws:
            if kw in song_title:
                return mood
    return "위로"


def parse(line: str):
    vid, _, title = line.partition("|")
    # "가수 - 곡명｜현역가왕3 ...｜CReAstudio" 형태
    head = title.split("｜")[0].split("|")[0].strip()
    if " - " in head:
        artist, song = head.split(" - ", 1)
    else:
        artist, song = "", head
    return vid.strip(), artist.strip(), song.strip()


